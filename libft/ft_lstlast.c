#include <stdlib.h> /*
# define NULL
#        */
#include "libft.h" /*
#typedef t_list;
#        */
/* **************************** [^] INCLUDES [^] **************************** */

t_list
	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
